    public void mostraTable() throws SQLException{
        
        DefaultTableModel model = (DefaultTableModel) tableCliente.getModel();
        model.setNumRows(0);
        ClienteDAO cdao = new ClienteDAO();
        
        for(Cliente c : cdao.listaCliente()){
            
            model.addRow(new Object[]{
                c.getCpf(),
                c.getNome(),
                c.getEndereco(),
                c.getTelefone(),
                c.getDataDeCadastro()
            });
        }

    }
